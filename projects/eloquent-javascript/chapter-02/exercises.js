// drawChessboard //////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function drawChessboard(x) {
  let str = '';
  for(let a = 0; a < x; a++){
    for(let b = 0; b < x; b++){
      if((a+b) % 2 === 0){
        str += ' ';
      } else {
        str += '#';
      }
    }
    str += '\n';
  }
console.log(str);
}

