// fizzBuzz ////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function fizzBuzz() {
  for (var i = 1;  i <= 15; i++){
    // if statement for mult of 3 & 5
    if(i % 3 === 0 && i % 5 === 0){
        // log 'FizzBuzz'
        console.log('fizzbuzz');
    }
    // if mult of 3
    else if(i % 3 === 0){
        // log 'Fizz'
        console.log('fizz');
    }
    // if mult of 5
    else if(i % 5 === 0){
        // log 'Buzz'
        console.log('buzz');
    } else {
        // else log index
        console.log(i);
    }
}

}

